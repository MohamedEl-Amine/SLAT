def get_matching_keys(dictionary, target_list):
    """
    Find all keys from dictionary whose values match elements in target_list.
    
    Args:
        dictionary: Dictionary to search in
        target_list: List of values to match
        
    Returns:
        Dictionary with matching key-value pairs
    """
    matching_dict = {key: value for key, value in dictionary.items() if value in target_list}
    return matching_dict

